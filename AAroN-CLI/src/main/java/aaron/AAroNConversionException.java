package aaron;

public class AAroNConversionException extends Exception {

    public AAroNConversionException() {
    }

    public AAroNConversionException(String message) {
        super(message);
    }

    public AAroNConversionException(String message, Throwable cause) {
        super(message, cause);
    }

    public AAroNConversionException(Throwable cause) {
        super(cause);
    }

    public AAroNConversionException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
